<h3>About me</h3>
I am a Ph.D. candidate in control theory at the Automatic Control Laboratory, ETH - Zurich, under the supervision of Prof. Maryam Kamgarpour. I received the Bachelor and Master degrees in Automation Engineering from the University of Bologna, both with honours, in 2014 and 2016 respectively. In 2016, I visited the Autonomous Systems Laboratory, ETH - Zurich as a student researcher to work on the control of small fixed-wing UAVs in windy environments. I  received   the   O. Hugo Schuck Best Paper Award in 2018. My current research interests   are centered around the theoretical challenges of optimal distributed control for constrained large-scale systems. I am also interested in game theoretic approaches to the control of  multi-agent dynamical systems  and in several  applications ranging from the electrical power grid to formations of autonomous vehicles .

